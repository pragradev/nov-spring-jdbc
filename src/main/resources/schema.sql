CREATE TABLE EMPLOYEE( EMPLOYEE_ID INT NOT NULL,
                        FIRST_NAME VARCHAR(255),
                        LAST_NAME VARCHAR(255),
                        EMAIL_ID VARCHAR(255),
                        PRIMARY KEY (EMPLOYEE_ID));
CREATE TABLE STUDENT( STUDENT_ID INT NOT NULL,
                       FIRST_NAME VARCHAR(255),
                       LAST_NAME VARCHAR(255),
                       EMAIL_ID VARCHAR(255),
                       PRIMARY KEY (STUDENT_ID));
